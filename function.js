//pembagian//
function bagi(a,b){
    hasilBagi = a / b;
    return hasilBagi;
}
var nilai1 = 100;
var nilai2 = 5;
var hasilPembagian = bagi(nilai1, nilai2);
console.log("Hasil Pembagian adalah " + hasilPembagian);


//perkalian//
function kali(a,b,c){
    hasilKali = a * b * c;
    return hasilKali;
}
var nilai1 = 20;
var nilai2 = 5;
var nilai3 = 10;
var hasilPerkalian = kali(nilai1, nilai2, nilai3);

console.log("Hasil Perkalian Adalah "+hasilPerkalian);